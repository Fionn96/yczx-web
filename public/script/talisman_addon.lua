----将a,b序列中a为0的项置后------------------------------------------------------
function ZLuaTal_SkillMakeSeq(a,b)
	if type(a)~="table" or type(b)~="table" then return a,b end 
	if table.getn(a)~=table.getn(b) then return a,b end 
	local xnum,ynum
	local c={}
	local d={}
	for xnum=1,table.getn(a) do
			c[xnum]=0
			d[xnum]=0	
	end 
	ynum=1
	for xnum=1,table.getn(a) do
		if a[xnum]~=0 then
			c[ynum]=a[xnum]
			d[ynum]=b[xnum]
			ynum=ynum+1
		end 
	end 	 	
	return c,d
end 
