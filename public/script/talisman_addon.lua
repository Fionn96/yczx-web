---接受导入的法宝ID信息，转化为职业和法宝级别（相应人物级别档位）
function ZLuaTal_Talisman_Trans_ID(Temp_TalismanID,origin)
	if type(Temp_TalismanID)~="number" or type(origin)~="number" then return false,false end
  local Temp_Player_Prof,Temp_Talisman_Lvl
	local Temp_TalismanSign=ZLuaTalAvailableTalismans()	----标志法宝ID的量
	
 	for _,v in pairs(Temp_TalismanSign) do
 		-- 法宝
 		if Temp_TalismanID == v.tmplID and v.talType == "fabao" then
			if			v.school ==  1 then Temp_Player_Prof= 1		--鬼王法宝
			elseif	v.school ==  2 then Temp_Player_Prof= 2		--合欢法宝
			elseif	v.school ==  3 then Temp_Player_Prof= 3		--青云法宝
			elseif	v.school ==  4 then Temp_Player_Prof= 4		--天音法宝
			elseif	v.school ==  5 then Temp_Player_Prof= 5		--鬼道法宝
			elseif	v.school ==  6 then Temp_Player_Prof= 6		--焚香法宝
			elseif  v.school ==  7 then Temp_Player_Prof= 7		--九黎法宝
			elseif  v.school ==  8 then Temp_Player_Prof= 8		--烈山法宝
			elseif  v.school ==  9 then Temp_Player_Prof= 9		--怀光法宝
			elseif  v.school == 10 then Temp_Player_Prof=10		--天华法宝
			elseif  v.school == 11 then Temp_Player_Prof=11		--太昊法宝
			elseif  v.school == 12 then Temp_Player_Prof=12		--辰皇法宝
			elseif  v.school == 13 then Temp_Player_Prof=13		--牵机法宝
			elseif  v.school == 14 then Temp_Player_Prof=14		--英招法宝
			elseif  v.school == 15 then Temp_Player_Prof=15		--破军法宝
			elseif  v.school == 16 then Temp_Player_Prof=16		--青罗法宝
			elseif  v.school == 17 then Temp_Player_Prof=17		--青罗法宝
			elseif  v.school == 18 then Temp_Player_Prof=18		--青罗法宝
			else
				if 			v.tmplID == 4703  then 	Temp_Player_Prof=21		--少侠法宝
				elseif 	v.tmplID == 30076 then 	Temp_Player_Prof=22		--神裔法宝
																			--Temp_Player_Prof=23		--飞剑
				elseif 	v.tmplID == 22906 then 	Temp_Player_Prof=24		--元宵灯(无技能)
				elseif 	v.tmplID == 58825 then 	Temp_Player_Prof=33		--灵族法宝
				else										 				Temp_Player_Prof=20		--通用法宝
				end
			end

			if 			v.requireLevel <= 1		then Temp_Talisman_Lvl = 0
			elseif	v.requireLevel <= 15	then Temp_Talisman_Lvl = 1
			elseif	v.requireLevel <= 75	then Temp_Talisman_Lvl = 2
			elseif	v.requireLevel <= 105	then Temp_Talisman_Lvl = 3
			elseif	v.requireLevel <= 135	then Temp_Talisman_Lvl = 4
			end
 		end
 		-- 飞宝  任务给的彩票飞宝，要区分是否彩票飞剑
 		if Temp_TalismanID == v.tmplID and (v.talType == "feijian" or v.talType == "yushou" or v.talType == "chibang") then
	  	Temp_Player_Prof=23
 			if 			v.fromLottery == true  then Temp_Talisman_Lvl=1
			elseif 	v.fromLottery == false then Temp_Talisman_Lvl=0
			end
 		end
 	end

 	if Temp_Player_Prof==nil or Temp_Talisman_Lvl==nil then
 		return false,false
 	end

	local a1Temptable={}    ----返回的技能数据1
	local a2Temptable={}    ----返回的技能数据2
	a1Temptable,a2Temptable= ZLuaTal_Talisman_Skill_Info(Temp_Player_Prof,Temp_Talisman_Lvl,origin)

	if ZLuaTal_CheckIsNumberTable(a1Temptable,0) and ZLuaTal_CheckIsNumberTable(a2Temptable,0) then
		return a1Temptable,a2Temptable
	else
    return false,true
		----如果返回的值不是table或者table中有小于0的数，则返回false false
	end

end
